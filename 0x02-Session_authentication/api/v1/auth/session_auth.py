#!/usr/bin/env python3
"""Module for the authentication in the app using
session authentication"""
from flask import request
from typing import List, TypeVar
from .auth import Auth
import base64
from models.user import User
import uuid


class SessionAuth(Auth):
    """Defining the class for Session auth"""
    user_id_by_session_id = {}

    def create_session(self, user_id: str = None) -> str:
        """method for creating the session object that
        stores the session info"""
        if user_id is None or not isinstance(user_id, str):
            return None
        session_id = str(uuid.uuid4())
        self.user_id_by_session_id[session_id] = user_id
        return session_id

    def user_id_for_session_id(self, session_id: str = None) -> str:
        """Returns a User id based on the session id"""
        if session_id is None or not isinstance(session_id, str):
            return None
        return self.user_id_by_session_id.get(session_id)

    def current_user(self, request=None) -> TypeVar('User'):
        """Returns a User instance based on cookie value"""
        session_id = self.session_cookie(request)
        user_id = self.user_id_for_session_id(session_id)
        user = User.get(user_id)
        return user

    def destroy_session(self, request=None):
        """Destroys the authenticated session.
        """
        session_id = self.session_cookie(request)
        user_id = self.user_id_for_session_id(session_id)
        if (request is None or session_id is None) or user_id is None:
            return False
        if session_id in self.user_id_by_session_id:
            del self.user_id_by_session_id[session_id]
        return True
